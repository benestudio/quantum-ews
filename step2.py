def amplification(n_qubits=4):
    circ = Circuit()
    circ.h(np.arange(n_qubits))
    circ.x([0,1,2,3]).h([0])
    circ.add(cccnot(targets=[3,1,2,0]))
    circ.h([0]).x([0,1,2,3]).h(np.arange(n_qubits))
    return circ

def quantum_hash():
  circ=Circuit()
  circ.cnot(4,0).cnot(0,2).cnot(1,3).cnot(5,1)
  return circ

def reverse_quantum_hash():
  circ=Circuit()
  circ.cnot(5,1).cnot(1,3).cnot(0,2).cnot(4,0)
  return circ

def find_input():
  circ = Circuit().x([6]).h([0,1,2,3,4,5,6])

  circ.add(quantum_hash())
  circ.add(ccccnot(targets=[0,1,2,3,6]))
  circ.add(reverse_quantum_hash())
  circ.add(amplification()) 

  circ.add(quantum_hash())
  circ.add(ccccnot(targets=[0,1,2,3,6]))
  circ.add(reverse_quantum_hash())
  circ.add(amplification()) 
  # print(circ)
  task = device.run(circ, shots=1)
  measurement = task.result().measurements[0] # one result if shots=1
  bitstring = ''.join(map(str,measurement))[:6] # we don't need the additional qbit
  return bitstring

